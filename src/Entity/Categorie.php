<?php


namespace App\Entity;

/**
 * Class Post
 * @package App\Entity(repositoryClass="App\Repository\CategoryRepository")
 */
class Categorie
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string",lenght=255)
     */
    private $title;

    /**
     * @ORM\ManyToOne(targetEntity="App\Entity\SubCategory",inversedBy="post")
     */
    private $sub_category;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id): void
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * @param mixed $title
     */
    public function setTitle($title): void
    {
        $this->title = $title;
    }

    /**
     * @return mixed
     */
    public function getSubCategory()
    {
        return $this->sub_category;
    }

    /**
     * @param mixed $sub_category
     */
    public function setSubCategory($sub_category): void
    {
        $this->sub_category = $sub_category;
    }
}